package com.bluemobi.decor.entity;

import java.io.Serializable;
import java.util.List;
import java.util.Map;

/**
 * 用户扩展图片集合实体类
 * Created by tuyh on 2015/7/10.
 */
public class UserImage implements Serializable {

    // 最近商品图列表
    private List<Map<String,Object>> goodsList;

    // 最近场景图
    private Map<String,Object> scene;

    // 最近系列图
    private Map<String,Object> series;

    public List<Map<String, Object>> getGoodsList() {
        return goodsList;
    }

    public void setGoodsList(List<Map<String, Object>> goodsList) {
        this.goodsList = goodsList;
    }

    public Map<String, Object> getScene() {
        return scene;
    }

    public void setScene(Map<String, Object> scene) {
        this.scene = scene;
    }

    public Map<String, Object> getSeries() {
        return series;
    }

    public void setSeries(Map<String, Object> series) {
        this.series = series;
    }
}
